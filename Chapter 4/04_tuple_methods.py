a = (5, 5.11, 'anekant', 5.11)

# Returns on applying new functions
print(len(a))
print(a.count(5.11)) # Find in count of the item
print(a.index('anekant')) # Find the index of the value
print(a * 2) # Concat the same tuple
print(a + a) # Adds the same tuple at the end
print(5 in a) # Finds
print(a[:2]) # Slice and returns the new tuple